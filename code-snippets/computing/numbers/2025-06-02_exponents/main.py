# Positive exponent: raising 2 to the 3rd power
result1 = 2 ** 3  # 2 * 2 * 2

# Negative exponent: reciprocal of the positive exponent
result2 = 2 ** -2  # 1 / (2 * 2)

# Exponent zero: any nonzero number to the power of 0 is 1
result3 = 5 ** 0  # Always 1

# Fractional exponent: square root of 9
result4 = 9 ** 0.5  # Same as sqrt(9)

# Negative fractional exponent: reciprocal of the square root
result5 = 16 ** -0.5  # 1 / sqrt(16)

print(result1)
print(type(result1))
print(result2)
print(type(result2))
print(result3)
print(type(result3))
print(result4)
print(type(result4))
print(result5)
print(type(result5))